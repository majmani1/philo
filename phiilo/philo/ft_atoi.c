#include "philo.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		m;
	size_t	nb;

	i = 0;
	m = 1;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = nb * 10 + str[i] - '0';
		if (m == 1 && (nb > 9223372036854775807))
			return (-1);
		else if (m == -1 && (nb > 9223372036854775807))
			return (0);
		i++;
	}
	return (nb * m);
}
